public class Sword {
    int damage;
    int dodge;

    public Sword(int damage, int dodge) {
        this.damage = damage;
        this.dodge = dodge;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getDodge() {
        return dodge;
    }

    public void setDodge(int dodge) {
        this.dodge = dodge;
    }
}
